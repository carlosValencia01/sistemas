                    <b class="titulo">Visión</b>
                    <br><br>
                    <ol>
                        <li type="square">ITTepic</li>
                            <p>
                                Lograr la consolidación como institución de alto desempeño en educación superior tecnológica, 
                                con posgrados, educación continua y a distancia, estableciendo alianzas estratégicas a nivel 
                                global y estrechando la relación con nuestra sociedad, con base en los principios y valores institucionales.
                            </p>
                        <li type="square">D SyC</li>
                            <p>
                                Ser un departamento con recursos humano consolidado en su formación profesional y académica, 
                                generador de investigación científica y tecnológica que responda a la demanda educativa 
                                superior tecnológica de calidad, así como al sector productivo, público y social del entorno 
                                regional, nacional e internacional.
                            </p>
                        <li type="square">PE ISC</li>
                            <p>
                                Tener un programa educativo  pertinente y de excelencia; líder en la formación de Ingenieros en 
                                Sistemas Computacionales, que den soluciones a las necesidades que demandan los grupos de interés 
                                en un marco regional, nacional e internacional y comprometidos con el desarrollo sustentable.  
                            </p>
                    </ol>
                    <b class="titulo">Objetivo general de carrera</b>       
                        <p>
                            Formar profesionistas líderes con visión estratégica y amplio sentido ético; capaz de diseñar, 
                            desarrollar, implementar y administrar tecnología computacional para aportar soluciones 
                            innovadoras en beneficio de la sociedad; en un contexto global, multidisciplinario y sostenible.

                        </p> 

                    <b class="titulo">Perfil de ingreso</b>       
                    <ol>
                        <li type="square">Criterio y razonamiento lógico para la solución de problemas.</li>
                        <li type="square">Habilidad y/o gusto por la investigación, análisis y síntesis de información.</li>
                        <li type="square">Expresión oral y escrita.</li>
                        <li type="square">Actitudes de: respeto, responsabilidad, honestidad.</li>
                        <li type="square">Habilidad y/o gusto por las ciencias básicas y el desarrollo de las tecnologías de la información.</li>
                    </ol>

                    <b class="titulo">Perfil de egreso</b>       
                    <ol>
                        <li>Implementa aplicaciones computacionales para solucionar problemas de diversos contextos, integrando diferentes 
                            tecnologías, plataformas o dispositivos.</li>
                        <li>Diseña, desarrolla y aplica modelos computacionales para solucionar problemas, mediante la selección y uso de 
                            herramientas matemáticas.</li>
                        <li>Diseña e implementa interfaces para la automatización de sistemas de hardware y desarrollo del software 
                            asociado.</li>
                        <li>Coordina y participa en equipos multidisciplinarios para la aplicación de soluciones innovadoras en 
                            diferentes contextos.</li>
                        <li>Diseña, implementa y administra bases de datos optimizando los recursos disponibles, conforme a las 
                            normas vigentes de manejo y seguridad de la información.</li>
                        <li>Desarrolla y administra software para apoyar la productividad y competitividad de las organizaciones 
                            cumpliendo con estándares de calidad.</li>
                        <li>Evalúa tecnologías de hardware para soportar aplicaciones de manera efectiva.</li>
                        <li>Detecta áreas de oportunidad empleando una visión empresarial para crear proyectos aplicando las 
                            Tecnologías de la Información y Comunicación.</li>
                        <li>Diseña, configura y administra redes de computadoras para crear soluciones de conectividad en la 
                            organización, aplicando las normas y estándares vigentes.</li>
                    </ol>

                    <b class="titulo">Objetivos Educacionales</b>       
                    <ol>
                        <li>El egresado será capaz de liderar proyectos de TI dentro de la organización.</li>
                        <li>El egresado propondrá soluciones a las organizaciones con visión estratégica.</li>
                        <li>El egresado podrá diseñar y/o desarrollar y/o implementar y/o administrar 
                            soluciones computacionales.</li>
                        <li>El egresado propondrá soluciones innovadoras para los procesos que se llevan a cabo en la organización.</li>
                        <li>El egresado identificará áreas de oportunidad que le permitan emprender proyectos sustentables 
                            relacionados con las tecnologías de la información y comunicaciones.</li>
                        <li>El egresado promoverá su capacitación constante para la aplicación del conocimiento adquirido 
                            en el ámbito laboral.</li>
                    </ol>

                    <b class="titulo">Atributos de egreso</b>       
